// Helper function to get language code from localStorage
export function getLanguageCode(): string {
  if (typeof window === 'undefined') return 'en';
  const stored = localStorage.getItem('selectedLanguage');
  if (stored) {
    try {
      const lang = JSON.parse(stored);
      const code = lang.code?.toLowerCase() || 'en';
      // Map UI language codes to API language codes
      const langMap: Record<string, string> = {
        'en': 'en',
        'ru': 'ru',
        'uz': 'uz',
        'fr': 'en', // French not available, fallback to English
        'de': 'en', // German not available, fallback to English
        'es': 'es',
      };
      return langMap[code] || 'en';
    } catch (e) {
      return 'en';
    }
  }
  return 'en';
}

// Get translation for a key
export function t(key: string, lang?: string): string {
  const language = lang || getLanguageCode();
  const keys = key.split('.');
  let value: any = translations[language as keyof typeof translations] || translations.en;
  
  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) {
      // Fallback to English
      value = translations.en;
      for (const k2 of keys) {
        value = value?.[k2];
      }
      break;
    }
  }
  
  return typeof value === 'string' ? value : key;
}

